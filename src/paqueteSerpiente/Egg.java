package paqueteSerpiente;

public class Egg {

    //Atributos
    private int posX;
    private int posY;

    //Constructor
    public Egg(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    //Getters y Setters
    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }

}
